class TransferPayer(BaseModel):
    """Optional payer-side transfer slots passed from the router."""

    model_config = ConfigDict(populate_by_name=True)

    card_no: str | int | None = Field(default=None, alias="cardNo")
    card_remark: str | None = Field(default=None, alias="cardRemark")


class TransferPayee(BaseModel):
    """Payee-side transfer slots passed from the router."""

    model_config = ConfigDict(populate_by_name=True)

    name: str | None = Field(default=None, alias="name")
    card_no: str | int | None = Field(default=None, alias="cardNo")
    card_remark: str | None = Field(default=None, alias="cardRemark")
    card_bank: str | None = Field(default=None, alias="cardBank")
    phone: str | int | None = Field(default=None, alias="phone")


class TransferDetails(BaseModel):
    """Amount-side transfer slots passed from the router."""

    model_config = ConfigDict(populate_by_name=True)

    amount: str | int | float | None = Field(default=None, alias="amount")
    ccy: str | None = Field(default=None, alias="ccy")


class TransferMoneyAgentRequest(ConfigVariablesRequest):
    """Agent request payload generated by the router field mapping."""

    model_config = ConfigDict(populate_by_name=True)

    customer: AgentCustomer = Field(default_factory=AgentCustomer)
    conversation: AgentConversationContext = Field(default_factory=AgentConversationContext)
    intent: AgentIntentContext = Field(default_factory=AgentIntentContext)
    payer: TransferPayer = Field(default_factory=TransferPayer)
    payee: TransferPayee = Field(default_factory=TransferPayee)
    transfer: TransferDetails = Field(default_factory=TransferDetails)

    def slot_value(self, name: str, fallback: Any = None) -> Any:
        slots_data = self.get_slots_data()
        if name in slots_data:
            return slots_data.get(name)
        return fallback

    def intent_context(self) -> AgentIntentContext:
        parsed = self.get_config_json_value("intent")
        if isinstance(parsed, dict):
            try:
                return AgentIntentContext.model_validate(parsed)
            except Exception:
                pass
        return self.intent

    def recent_messages(self) -> list[str]:
        parsed = self.get_config_json_value("recent_messages")
        if isinstance(parsed, list):
            return [str(item) for item in parsed]
        return list(self.conversation.recent_messages)

    def long_term_memory_items(self) -> list[str]:
        parsed = self.get_config_json_value("long_term_memory")
        if isinstance(parsed, list):
            return [str(item) for item in parsed]
        return list(self.conversation.long_term_memory)


